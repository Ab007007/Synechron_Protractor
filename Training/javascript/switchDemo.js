console.log("Welcome to Switch conditional block");

let rating = 5;

switch (rating) {

    case 1:
        console.log("Very Bad Performance");
        
        break;
    case 2:
        console.log("Good Performance");
        
        break;
    case 3:
        console.log('Very Good');
        
        break; 
    case 4:
        console.log('Out Standing!!!');
        
        break;

    default:
        console.log('Verify your choice!!!! OR Rating not listed!!');
        
}
